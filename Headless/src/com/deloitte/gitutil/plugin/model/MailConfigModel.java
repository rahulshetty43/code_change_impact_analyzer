package com.deloitte.gitutil.plugin.model;

public class MailConfigModel {
	
	private String username;
	private String password;
	private String smtp_host;
	private String smtp_auth;
	private String smtp_starttls_enable;
	private String smtp_socketFactory_port;
	private String smtp_port;
	private String mail_to;
	private String subject;
	private String body;
	private String regards;
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getSmtp_host() {
		return smtp_host;
	}
	public void setSmtp_host(String smtp_host) {
		this.smtp_host = smtp_host;
	}
	public String getSmtp_auth() {
		return smtp_auth;
	}
	public void setSmtp_auth(String smtp_auth) {
		this.smtp_auth = smtp_auth;
	}
	public String getSmtp_starttls_enable() {
		return smtp_starttls_enable;
	}
	public void setSmtp_starttls_enable(String smtp_starttls_enable) {
		this.smtp_starttls_enable = smtp_starttls_enable;
	}
	public String getSmtp_socketFactory_port() {
		return smtp_socketFactory_port;
	}
	public void setSmtp_socketFactory_port(String smtp_socketFactory_port) {
		this.smtp_socketFactory_port = smtp_socketFactory_port;
	}
	public String getSmtp_port() {
		return smtp_port;
	}
	public void setSmtp_port(String smtp_port) {
		this.smtp_port = smtp_port;
	}
	public String getMail_to() {
		return mail_to;
	}
	public void setMail_to(String mail_to) {
		this.mail_to = mail_to;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getBody() {
		return body;
	}
	public void setBody(String body) {
		this.body = body;
	}
	public String getRegards() {
		return regards;
	}
	public void setRegards(String regards) {
		this.regards = regards;
	}
	
	
	

}
